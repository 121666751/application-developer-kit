# 请求文件日志
CREATE TABLE FILE_REQ_NOTIFY_LOG (
	ID	INTEGER	PRIMARY KEY,
	REQ_ID	VARCHAR(20)	NOT NULL,
	RSP_ID	VARCHAR(20),
	TENANT  VARCHAR(32) NOT NULL,
	BIZ_TYPE	VARCHAR(64)	NOT NULL,
	FILE_SERVICE	VARCHAR(8)	NOT NULL,
	STATUS	VARCHAR(16)	NOT NULL,
	STATE	VARCHAR(32)	NOT NULL,
	ERROR_CODE	VARCHAR(32),
	ERROR_MSG		VARCHAR(512),
	FILE_PATH	VARCHAR(1024)	NOT NULL,
	FILE_NAME	VARCHAR(128)	NOT NULL,
	FILE_TIME	DATETIME NOT NULL,
	LOCAL_FILE_PATH	VARCHAR(1024),
	LOCAL_FILE_NAME	VARCHAR(128),
	LOCAL_FILE_TIME	DATETIME,
	CONFIRM_DATA	VARCHAR(216),
	DUBBO_GROUP	VARCHAR(32),
	DUBBO_VERSION	VARCHAR(16),
	PARTNER_ID	VARCHAR(20) NOT NULL,
	GID	VARCHAR(35) NOT NULL,
	MERCHANT_ORDER_NO	VARCHAR(64),
	RAW_ADD_TIME  DATETIME NOT NULL,
  RAW_UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) COMMENT '请求文件日志';
CREATE UNIQUE INDEX UIDX_REQ_REQID ON FILE_REQ_NOTIFY_LOG(REQ_ID,TENANT);


