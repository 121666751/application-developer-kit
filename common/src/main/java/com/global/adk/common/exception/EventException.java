package com.global.adk.common.exception;

public class EventException extends KitNestException {
	
	private static final long serialVersionUID = -4902678466088758477L;
	
	public EventException() {
		
		super();
	}
	
	public EventException(String arg0, Throwable arg1) {
		
		super(arg0, arg1);
	}
	
	public EventException(String arg0) {
		
		super(arg0);
	}
	
	public EventException(Throwable arg0) {
		
		super(arg0);
	}
	
}
